	.module crt0
	.globl	_main

	.area	_HEADER (ABS)
	.org 	0x1300
	JP go
	.org 	0x133E
	;; Set stack pointer directly above top of memory.
go:
	ld	sp,#0x0000
	call	_main
	JP 0x1500

	;; Ordering of segments for the linker.
	.area	_CODE
	.area	_DATA
	

	





